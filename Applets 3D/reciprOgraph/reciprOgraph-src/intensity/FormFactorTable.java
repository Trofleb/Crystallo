	public double scat2(int n, double h) {
		double f = c[n-1];
		for(int i=0; i<4; i++)
			f += a[n-1][i] * Math.exp(-b[n-1][i]*h);
		//System.out.println("n="+(n)+" h/2="+(h/2)+" f="+f);
		return f;
	}

